import lombok.Getter;
import lombok.Builder;

/**
 * Maximum number of packages that can be stored at this Access Point location.
 * If the value is not present the threshold will be set by default to 20
 * packages.
 * Minimum supported value is 5 packages.
 */
@Getter
@Builder
public class Capacity {
    private final Integer threshold;
}
